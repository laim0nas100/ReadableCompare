package lt.lb.readablecompare;

/**
 *
 * @author laim0nas100
 */
public enum CompareOperator {
    /**
     * {@code <} operator
     */
    LESS,
    /**
     * {@code <=} operator
     */
    LESS_EQ,
    /**
     * {@code >} operator
     */
    GREATER,
    /**
     * {@code >=} operator
     */
    GREATER_EQ,
    /**
     * {@code ==} operator
     */
    EQ,
    /**
     * {@code !=} operator
     */
    NOT_EQ
}
